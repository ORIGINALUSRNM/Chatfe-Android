package com.chatfe;

public class TopicData {

	public String topic = null;
	public String topicImg = null;
	
	public void setTopic(String newtopic){
		
		topic = newtopic;
		
	}
	
	public String getTopic(){
		
		return topic;
	}
	
	public void setTopicImg(String newtopicImg){
		
		topicImg = newtopicImg;
		
	}
	
	public String getTopicImg(){
		
		return topicImg;
	}
}
